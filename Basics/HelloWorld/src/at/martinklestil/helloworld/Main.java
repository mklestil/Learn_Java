package at.martinklestil.helloworld;

public class Main {

    public static void main(String[] args) {
        System.out.println("Hello World!");

        /*
        public: Zugriffsmodifikator
        static: Java Schlüsselwort, Methode verwendbar ohne ein Objekt der Klasse zu erstellen.
        void: Rückgabetyp der Methode, gibt keinen Wert zurück.
        main: Name der Methode, JVM Einstiegspunkt für die Anwengung
        String args[]: Parameter die an die main Methode überweben werden.
        * */
    }
}
